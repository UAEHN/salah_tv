String adhanSoundLabel(String soundId) {
  switch (soundId) {
    case 'ali_mulla':
      return 'Ali Mulla';
    case 'abdulbasit':
      return 'Abdulbasit Abdulsamad';
    case 'aqsa':
      return 'Al-Aqsa Adhan';
    case 'default':
      return 'Adhan 1';
    case 'adhan2':
      return 'Adhan 2';
    default:
      return 'Adhan 1';
  }
}
