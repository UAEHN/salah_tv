/// Country English key → Arabic display name.
String countryLabel(String key) {
  for (final c in kCountries) {
    if (c.key == key) return c.arabicName;
  }
  return key;
}

/// Find which country a city belongs to. Returns the country key or ''.
String countryForCity(String cityKey) {
  for (final c in kCountries) {
    if (c.cities.contains(cityKey)) return c.key;
  }
  return '';
}

/// Get cities that belong to a specific country AND are available in the CSV.
List<String> citiesForCountry(String countryKey, List<String> availableCities) {
  for (final c in kCountries) {
    if (c.key == countryKey) {
      return availableCities.where((city) => c.cities.contains(city)).toList();
    }
  }
  return availableCities;
}

