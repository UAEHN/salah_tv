// djb2-style hash (same algorithm as csv_to_json.dart)
function computeHash(rows) {
  let h = 5381;
  for (const row of rows) {
    for (const v of row) {
      h = (Math.imul(h, 33) ^ v) >>> 0;
    }
  }
  return h.toString(16).padStart(8, '0');
}

function toMin(t) {
  const [hh, mm] = t.split(':').map(Number);
  return hh * 60 + mm;
}

// Month lengths for 2026 (not a leap year)
const MONTH_DAYS = [31,28,31,30,31,30,31,31,30,31,30,31];

const rows = [];
for (let m = 0; m < 12; m++) {
  const days = MONTH_DAYS[m];
  for (let d = 0; d < days; d++) {
    const parts = RAW[m][d].split(' ');
    // PDF order: [isha, maghrib, asr, dhuhr, sunrise, fajr, imsak]
    // indices:      0       1     2      3        4      5      6
    const isha    = toMin(parts[0]);
    const maghrib = toMin(parts[1]);
    const asr     = toMin(parts[2]);
    const dhuhr   = toMin(parts[3]);
    const sunrise = toMin(parts[4]);
    const fajr    = toMin(parts[5]);

    const year  = 2026;
    const month = m + 1;
    const day   = d + 1;
    const date  = year * 10000 + month * 100 + day;

    rows.push([date, fajr, sunrise, dhuhr, asr, maghrib, isha]);
  }
}

